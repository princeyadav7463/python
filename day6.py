# type casting or type conversion .

'''Type casting
The conversion of one data type into the other data type is known as type casting in python or type conversion in python.

pyton supports a wide variety of functions or methods like: int(), float(), str(), ord(), hex(), oct(), tuple(), set(), list(), dict(), etc. for the type casting in python.

# Two types of typecasting
explicit conversion (explicit type casting) - done via methods.
implicit conversion (implicit type casting) - automatically done by python.
'''

a = "1"
b = "2"
print(a+b)

print(int(a)+int(b))   # explicit type casting  
# Implicit type casting

c = 1.9
d = 8
print(c+d)

